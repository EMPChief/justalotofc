#include <stdio.h>

int main()
{
    // for loop - loops through a block of code a specified number of times
    // for exempel i will be printed 10 times so 0 to 9 will be printed if want the 10 numbers in a row use for (int i = 0; i <= 10; i++)
    for (int i = 0; i <= 10; i++)
    {
        printf("%d\n", i);
    }

    return 0;

}